        [HttpPost("~/api/Requirement/Requirement/1/InsertOrUpdateAsync")]
        
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["requirement-requirement-title-page"];

                string Title = HttpContext.Request.Form["requirement-requirement-title-input"];
                string Body = HttpContext.Request.Form["requirement-requirement-body-input"];
                int RequirementStateId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-requirementstateid-input"]) != 0)
                {
                    RequirementStateId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-requirementstateid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in RequirementStateId"); }
                int RequirementPriorityId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-requirementpriorityid-input"]) != 0)
                {
                    RequirementPriorityId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-requirementpriorityid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in RequirementPriorityId"); }
                int UserEmployeeId = 0;
                if (!AddOrEdit.StartsWith("Add"))
                {
                    if (Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-useremployeeid-input"]) != 0)
                    {
                        UserEmployeeId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-useremployeeid-input"]);
                    }
                    else
                    { return StatusCode(400, "It's not allowed to save zero values in UserEmployeeId"); }
                }

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (AddOrEdit.StartsWith("Add"))
                {
                    //Add
                    RequirementModel RequirementModel = new RequirementModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        Title = Title,
                        Body = Body,
                        RequirementStateId = RequirementStateId,
                        RequirementPriorityId = RequirementPriorityId,
                        UserEmployeeId = UserEmployeeId,
                        
                    };
                    
                    NewEnteredId = _RequirementProtocol.Insert(RequirementModel);
                }
                else
                {
                    //Update
                    int RequirementId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirement-requirementid-input"]);
                    RequirementModel RequirementModel = new RequirementModel(RequirementId);
                    
                    RequirementModel.UserLastModificationId = UserId;
                    RequirementModel.DateTimeLastModification = DateTime.Now;
                    RequirementModel.Title = Title;
                    RequirementModel.Body = Body;
                    RequirementModel.RequirementStateId = RequirementStateId;
                    RequirementModel.RequirementPriorityId = RequirementPriorityId;
                    RequirementModel.UserEmployeeId = UserEmployeeId;
                                       

                    RowsAffected = _RequirementProtocol.UpdateByRequirementId(RequirementModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/Requirement/Requirement/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

