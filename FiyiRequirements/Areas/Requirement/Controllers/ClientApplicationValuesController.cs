        [HttpPost("~/api/Requirement/ClientApplication/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["requirement-clientapplication-title-page"];

                int ClientId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["requirement-clientapplication-clientid-input"]) != 0)
                {
                    ClientId = Convert.ToInt32(HttpContext.Request.Form["requirement-clientapplication-clientid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ClientId"); }
                int ApplicationId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["requirement-clientapplication-applicationid-input"]) != 0)
                {
                    ApplicationId = Convert.ToInt32(HttpContext.Request.Form["requirement-clientapplication-applicationid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ApplicationId"); }
                

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (AddOrEdit.StartsWith("Add"))
                {
                    //Add
                    ClientApplicationModel ClientApplicationModel = new ClientApplicationModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        ClientId = ClientId,
                        ApplicationId = ApplicationId,
                        
                    };
                    
                    NewEnteredId = _ClientApplicationProtocol.Insert(ClientApplicationModel);
                }
                else
                {
                    //Update
                    int ClientApplicationId = Convert.ToInt32(HttpContext.Request.Form["requirement-clientapplication-clientapplicationid-input"]);
                    ClientApplicationModel ClientApplicationModel = new ClientApplicationModel(ClientApplicationId);
                    
                    ClientApplicationModel.UserLastModificationId = UserId;
                    ClientApplicationModel.DateTimeLastModification = DateTime.Now;
                    ClientApplicationModel.ClientId = ClientId;
                    ClientApplicationModel.ApplicationId = ApplicationId;
                                       

                    RowsAffected = _ClientApplicationProtocol.UpdateByClientApplicationId(ClientApplicationModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/Requirement/ClientApplication/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = 1,
                    UserLastModificationId = 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

