        [HttpPost("~/api/Requirement/Client/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["requirement-client-title-page"];

                string FirstName = HttpContext.Request.Form["requirement-client-firstname-input"];
                string LastName = HttpContext.Request.Form["requirement-client-lastname-input"];
                string BusinessName = HttpContext.Request.Form["requirement-client-businessname-input"];
                string PhoneNumber = HttpContext.Request.Form["requirement-client-phonenumber-input"];
                string Email = HttpContext.Request.Form["requirement-client-email-input"];
                

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (AddOrEdit.StartsWith("Add"))
                {
                    //Add
                    ClientModel ClientModel = new ClientModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        FirstName = FirstName,
                        LastName = LastName,
                        BusinessName = BusinessName,
                        PhoneNumber = PhoneNumber,
                        Email = Email,
                        
                    };
                    
                    NewEnteredId = _ClientProtocol.Insert(ClientModel);
                }
                else
                {
                    //Update
                    int ClientId = Convert.ToInt32(HttpContext.Request.Form["requirement-client-clientid-input"]);
                    ClientModel ClientModel = new ClientModel(ClientId);
                    
                    ClientModel.UserLastModificationId = UserId;
                    ClientModel.DateTimeLastModification = DateTime.Now;
                    ClientModel.FirstName = FirstName;
                    ClientModel.LastName = LastName;
                    ClientModel.BusinessName = BusinessName;
                    ClientModel.PhoneNumber = PhoneNumber;
                    ClientModel.Email = Email;
                                       

                    RowsAffected = _ClientProtocol.UpdateByClientId(ClientModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/Requirement/Client/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

