        [HttpPost("~/api/Requirement/RequirementFile/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["requirement-requirementfile-title-page"];

                int RequirementId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["requirement-requirementfile-requirementid-input"]) != 0)
                {
                    RequirementId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirementfile-requirementid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in RequirementId"); }
                string FileName = HttpContext.Request.Form["requirement-requirementfile-filename-input"];
                string FilePath = HttpContext.Request.Form["requirement-requirementfile-filepath-input"];;
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    FilePath = $@"/Uploads/Requirement/RequirementFile/{HttpContext.Request.Form.Files[0].FileName}";
                }
                

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (AddOrEdit.StartsWith("Add"))
                {
                    //Add
                    RequirementFileModel RequirementFileModel = new RequirementFileModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        RequirementId = RequirementId,
                        FileName = FileName,
                        FilePath = FilePath,
                        
                    };
                    
                    NewEnteredId = _RequirementFileProtocol.Insert(RequirementFileModel);
                }
                else
                {
                    //Update
                    int RequirementFileId = Convert.ToInt32(HttpContext.Request.Form["requirement-requirementfile-requirementfileid-input"]);
                    RequirementFileModel RequirementFileModel = new RequirementFileModel(RequirementFileId);
                    
                    RequirementFileModel.UserLastModificationId = UserId;
                    RequirementFileModel.DateTimeLastModification = DateTime.Now;
                    RequirementFileModel.RequirementId = RequirementId;
                    RequirementFileModel.FileName = FileName;
                    RequirementFileModel.FilePath = FilePath;
                                       

                    RowsAffected = _RequirementFileProtocol.UpdateByRequirementFileId(RequirementFileModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/Requirement/RequirementFile/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

